	void Funny::printfLine(char *str, uint8_t line) {

        	uint16_t attrib = 0x07;

        	volatile uint16_t* vidmem;
		

        	for (int i = 0; str[i] != '\0'; i++) {

        		vidmem = (volatile uint16_t*)0xb8000 + (80*line+i);
	                *vidmem = str[i] | (attrib << 8);

        	}
	}


